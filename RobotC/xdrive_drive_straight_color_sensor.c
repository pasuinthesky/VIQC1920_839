	  	selector_b = 1;
	  	break;

		case 2:
		  motor_a = FL;
		  motor_b = BR;
	  	selector_a = 1;
	  	selector_b = 1;
	  	break;

		case 3:
		  if(distance > 0)
		  {
		  	motor_a = BR;
		  	motor_b = BL;
			}
			else
			{
			 	motor_a = FR;
		  	motor_b = FL;
			}
	  	selector_a = 1;
	  	selector_b = 0;
